// Comprehensive Examples for Arrays in JavaScript

// Example 1: Creating and Initializing an Array
let fruits = ["apple", "banana", "cherry"]; // Array of strings
let numbers = [1, 2, 3, 4, 5]; // Array of numbers
let mixedArray = [1, "two", true, null]; // Array with mixed data types

console.log(`Fruits: ${fruits} (${typeof fruits}), Numbers: ${numbers} (${typeof numbers}), Mixed Array: ${mixedArray} (${typeof mixedArray})`);

// Example 2: Accessing Elements in an Array
console.log(`First fruit: ${fruits[0]}`); // Output: apple
console.log(`Last number: ${numbers[numbers.length - 1]}`); // Output: 5

